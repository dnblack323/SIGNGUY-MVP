async def get_payroll_settings(*, tenant_id: str) -> dict[str, Any]:
    from . import settings as settings_service
    stored = await settings_service.list_namespace(tenant_id=tenant_id, namespace="payroll")
    return {**DEFAULT_PAYROLL_SETTINGS, **stored}


async def update_payroll_settings(*, tenant_id: str, updates: dict[str, Any], updated_by: str) -> dict[str, Any]:
    from . import settings as settings_service
    allowed = set(DEFAULT_PAYROLL_SETTINGS.keys())
    clean: dict[str, Any] = {}
    for k, v in updates.items():
        if k not in allowed:
            raise PayrollError(400, f"Unknown payroll setting: {k}")
        if k == "default_payday" and v != "friday":
            raise PayrollError(400, "default_payday is locked to 'friday' in v1")
        if k == "work_week_start" and v != "saturday":
            raise PayrollError(400, "work_week_start is locked to 'saturday' in v1")
        if k == "weekly_threshold_minutes" and (not isinstance(v, (int, float)) or v <= 0):
            raise PayrollError(400, "weekly_threshold_minutes must be a positive number")
        if k == "overtime_multiplier" and (not isinstance(v, (int, float)) or v < 1.0):
            raise PayrollError(400, "overtime_multiplier must be >= 1.0")
        clean[k] = v
    if not clean:
        raise PayrollError(400, "No valid settings provided")
    await settings_service.set_many(tenant_id=tenant_id, namespace="payroll", values=clean, updated_by=updated_by)
    return await get_payroll_settings(tenant_id=tenant_id)


def _resolve_overtime_policy(tenant_settings: dict[str, Any], employee: dict[str, Any]) -> dict[str, Any]:
    override = employee.get("overtime_override")
    if isinstance(override, dict) and override:
        return {
            "enabled": bool(override.get("enabled", tenant_settings["overtime_enabled"])),
            "weekly_threshold_minutes": int(override.get("weekly_threshold_minutes", tenant_settings["weekly_threshold_minutes"])),
            "multiplier": float(override.get("multiplier", tenant_settings["overtime_multiplier"])),
            "source": "employee_override",
        }
    return {
        "enabled": bool(tenant_settings["overtime_enabled"]),
        "weekly_threshold_minutes": int(tenant_settings["weekly_threshold_minutes"]),
        "multiplier": float(tenant_settings["overtime_multiplier"]),
        "source": "tenant_default",
    }


