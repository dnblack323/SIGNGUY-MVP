"""EC8 phase 8d — Payroll service (canonical gross-pay ledger engine).

Scope boundary (LOCKED — owner-approved EC8 preflight + Phase 8d gate):
This is an internal workforce gross-pay ledger — NOT a payroll-processing,
tax-filing, or banking service. No ACH, no direct deposit, no SSN/bank
storage, no tax withholding or statutory deductions, no W-2/1099
generation. Overtime math is a configurable internal estimate only — never
represented as legal/compliance payroll.

Architecture:
- `PayPeriod` — one per tenant per Saturday-Friday week (payday == end_date,
  always the Friday). Uniqueness enforced by a DB index on
  (tenant_id, start_date) — this is what structurally prevents overlapping
  periods (every lookup/creation normalizes through
  `week_bounds_for_date_str` first).
- `PayrollSnapshot` — one per (period, employee); a pure READ MODEL derived
  from the `PayrollTransaction` ledger (see `_sum_ledger`). Hours/rate are
  computed fresh on each recalculation while the period is open/review;
  money totals always equal a live sum over the ledger.
- `PayrollTransaction` — append-only ledger; the single source of truth.
  Corrections/voids never mutate a historical row's `amount_cents` — they
  add a new offsetting `type="void"` row and flip `voided=True` on the
  original (see `_void_transaction_row`).
- Financial status (`partially_paid` / `paid`) is NEVER set directly by an
  API caller — it is always derived from the ledger by
  `_refresh_period_financial_status`.
"""
