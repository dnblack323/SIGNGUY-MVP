async def _get_period(tenant_id: str, period_id: str) -> dict:
    doc = await db.pay_periods.find_one({"id": period_id, "tenant_id": tenant_id}, {"_id": 0})
    if not doc:
        raise PayrollError(404, "Pay Period not found")
    return doc


async def get_or_create_pay_period(*, tenant_id: str, period_start: str, actor_user_id: str) -> dict:
    week_start, week_end = week_bounds_for_date_str(period_start)
    existing = await db.pay_periods.find_one({"tenant_id": tenant_id, "start_date": week_start}, {"_id": 0})
    if existing:
        await _link_pending_carryovers(tenant_id, existing, actor_user_id)
        doc = await db.pay_periods.find_one({"id": existing["id"], "tenant_id": tenant_id}, {"_id": 0})
        return serialize_doc(doc or {})
    doc = PayPeriod(
        tenant_id=tenant_id, start_date=week_start, end_date=week_end, payday=week_end,
        created_by=actor_user_id, updated_by=actor_user_id,
    ).model_dump()
    await db.pay_periods.insert_one(prepare_for_mongo(dict(doc)))
    doc.pop("_id", None)
    await _link_pending_carryovers(tenant_id, doc, actor_user_id)
    refreshed = await db.pay_periods.find_one({"id": doc["id"], "tenant_id": tenant_id}, {"_id": 0})
    return serialize_doc(refreshed or doc)


async def _period_summary(tenant_id: str, period_id: str) -> dict:
    snaps = await list_period_snapshots(tenant_id=tenant_id, period_id=period_id)
    return {
        "employee_count": len(snaps),
        "total_gross_cents": sum(s["gross_regular_cents"] + s["gross_overtime_cents"] for s in snaps),
        "total_paid_cents": sum(s["total_paid_cents"] for s in snaps),
        "total_remaining_cents": sum(s["remaining_balance_cents"] for s in snaps),
        "blocking_warning_count": sum(1 for s in snaps if s.get("warnings")),
    }


async def list_pay_periods(*, tenant_id: str, status: Optional[str] = None, limit: int = 200) -> list[dict]:
    filt: dict[str, Any] = {"tenant_id": tenant_id}
    if status:
        filt["status"] = status
    cur = db.pay_periods.find(filt, {"_id": 0}).sort("start_date", -1).limit(limit)
    periods = [serialize_doc(d) async for d in cur]
    for p in periods:
        p["summary"] = await _period_summary(tenant_id, p["id"])
    return periods


async def get_pay_period_detail(*, tenant_id: str, period_id: str) -> dict:
    period = await _get_period(tenant_id, period_id)
    snapshots = await list_period_snapshots(tenant_id=tenant_id, period_id=period_id)
    return {"period": serialize_doc(period), "snapshots": snapshots, "summary": await _period_summary(tenant_id, period_id)}


