async def add_manual_transaction(*, tenant_id: str, pay_period_id: str, employee_id: str, type: str,
                                  amount_cents: int, effective_date: str, actor_user_id: str, actor_email: str,
                                  reference: Optional[str] = None, notes: Optional[str] = None,
                                  payment_method: Optional[str] = None, payment_date: Optional[str] = None,
                                  idempotency_key: Optional[str] = None) -> dict:
    if type not in MANUAL_TRANSACTION_TYPES:
        raise PayrollError(400, f"type must be one of {sorted(MANUAL_TRANSACTION_TYPES)}")
    if not amount_cents or int(amount_cents) == 0:
        raise PayrollError(400, "amount_cents must be non-zero")
    period = await _get_period(tenant_id, pay_period_id)
    if period["status"] in ("closed", "voided"):
        raise PayrollError(409, f"Pay Period is {period['status']} — cannot add transactions")
    emp = await db.employees.find_one({"id": employee_id, "tenant_id": tenant_id}, {"_id": 0, "name": 1})
    if not emp:
        raise PayrollError(404, "Employee not found")
    if idempotency_key:
        existing = await db.payroll_transactions.find_one({"tenant_id": tenant_id, "idempotency_key": idempotency_key, "voided": False}, {"_id": 0})
        if existing:
            return serialize_doc(existing)
    doc = PayrollTransaction(
        tenant_id=tenant_id, employee_id=employee_id, pay_period_id=pay_period_id, type=type,
        amount_cents=int(amount_cents), effective_date=effective_date, reference=reference, notes=notes,
        payment_method=payment_method, payment_date=payment_date, idempotency_key=idempotency_key,
        created_by=actor_user_id,
    ).model_dump()
    await db.payroll_transactions.insert_one(prepare_for_mongo(dict(doc)))
    await recompute_snapshot_totals(tenant_id=tenant_id, period_id=pay_period_id, employee_id=employee_id)
    await record_activity_with_audit(
        tenant_id=tenant_id, actor_user_id=actor_user_id, actor_email=actor_email,
        module="team", action=f"payroll.transaction_{type}", entity_type="payroll_transaction", entity_id=doc["id"],
        summary=f"{type.replace('_', ' ').title()} of {amount_cents} cents recorded for {emp['name']}",
        severity="warning" if type in ("advance", "advance_repayment") else "info",
    )
    created = await db.payroll_transactions.find_one({"id": doc["id"], "tenant_id": tenant_id}, {"_id": 0})
    return serialize_doc(created or doc)


async def void_manual_transaction(*, tenant_id: str, transaction_id: str, actor_user_id: str, actor_email: str, reason: str) -> dict:
    if not reason or not reason.strip():
        raise PayrollError(400, "A reason is required to void a transaction")
    txn = await db.payroll_transactions.find_one({"id": transaction_id, "tenant_id": tenant_id}, {"_id": 0})
    if not txn:
        raise PayrollError(404, "Transaction not found")
    if txn["type"] in NON_VOIDABLE_TYPES:
        raise PayrollError(400, f"{txn['type']} transactions cannot be voided directly")
    if txn.get("voided"):
        raise PayrollError(400, "Transaction is already voided")
    period = await _get_period(tenant_id, txn["pay_period_id"])
    if period["status"] == "closed":
        raise PayrollError(409, "Cannot void a transaction in a closed Pay Period")
    now_iso = utc_now().isoformat()
    reversal = PayrollTransaction(
        tenant_id=tenant_id, employee_id=txn["employee_id"], pay_period_id=txn["pay_period_id"], type="void",
        amount_cents=-txn["amount_cents"], effective_date=now_iso[:10],
        source_record_type="payroll_transaction", source_record_id=txn["id"], notes=reason, created_by=actor_user_id,
    ).model_dump()
    await db.payroll_transactions.insert_one(prepare_for_mongo(dict(reversal)))
    await db.payroll_transactions.update_one(
        {"id": transaction_id, "tenant_id": tenant_id},
        {"$set": {"voided": True, "voided_by": actor_user_id, "voided_at": now_iso, "void_reason": reason}},
    )
    await recompute_snapshot_totals(tenant_id=tenant_id, period_id=txn["pay_period_id"], employee_id=txn["employee_id"])
    await record_activity_with_audit(
        tenant_id=tenant_id, actor_user_id=actor_user_id, actor_email=actor_email,
        module="team", action="payroll.transaction_voided", entity_type="payroll_transaction", entity_id=transaction_id,
        summary=f"Transaction voided: {reason}", severity="warning",
    )
    return await recompute_snapshot_totals(tenant_id=tenant_id, period_id=txn["pay_period_id"], employee_id=txn["employee_id"])


async def list_transactions(*, tenant_id: str, employee_id: Optional[str] = None, pay_period_id: Optional[str] = None,
                             include_voided: bool = False, limit: int = 500) -> list[dict]:
    filt: dict[str, Any] = {"tenant_id": tenant_id}
    if employee_id:
        filt["employee_id"] = employee_id
    if pay_period_id:
        filt["pay_period_id"] = pay_period_id
    if not include_voided:
        filt["voided"] = False
    cur = db.payroll_transactions.find(filt, {"_id": 0}).sort([("effective_date", -1), ("created_at", -1)]).limit(limit)
    return [serialize_doc(d) async for d in cur]


async def list_employee_snapshots(*, tenant_id: str, employee_id: str, limit: int = 200) -> list[dict]:
    cur = db.payroll_snapshots.find({"tenant_id": tenant_id, "employee_id": employee_id}, {"_id": 0}).limit(limit)
    snaps = [serialize_doc(d) async for d in cur]
    period_ids = list({s["pay_period_id"] for s in snaps})
    periods = {p["id"]: p async for p in db.pay_periods.find({"tenant_id": tenant_id, "id": {"$in": period_ids}}, {"_id": 0})}
    for s in snaps:
        p = periods.get(s["pay_period_id"], {})
        s["period_start"] = p.get("start_date")
        s["period_end"] = p.get("end_date")
        s["payday"] = p.get("payday")
        s["period_status"] = p.get("status")
    snaps.sort(key=lambda s: s.get("period_start") or "", reverse=True)
    return snaps
