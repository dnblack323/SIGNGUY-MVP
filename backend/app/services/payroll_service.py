# Ledger transaction types a manager may enter directly. `earning` /
# `overtime_earning` are only ever system-generated by recalculation.
# `carryover_in` / `carryover_out` are only ever system-generated by
# `close_period`. `correction` / `void` are reserved for the reversal
# machinery below.
MANUAL_TRANSACTION_TYPES = {"adjustment", "advance", "advance_repayment", "payment"}
# Types that may never be voided directly through the manual-void endpoint —
# `earning`/`overtime_earning` are only ever superseded by recalculation,
# and a `void` row can never itself be voided (no double-reversal).
NON_VOIDABLE_TYPES = {"earning", "overtime_earning", "void"}

DEFAULT_PAYROLL_SETTINGS: dict[str, Any] = {
    "overtime_enabled": True,
    "weekly_threshold_minutes": 2400,   # 40 hours
    "overtime_multiplier": 1.5,
    "rounding_policy": "nearest_cent",  # informational only — money is always integer cents
    "default_payday": "friday",         # LOCKED — v1 supports Friday payday only
    "work_week_start": "saturday",      # LOCKED — v1 supports Saturday-start weeks only
}


class PayrollError(Exception):
    def __init__(self, status_code: int, detail: str):
        self.status_code = status_code
        self.detail = detail
        super().__init__(detail)


