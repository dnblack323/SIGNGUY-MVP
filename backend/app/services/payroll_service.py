def _overlaps(a_start: str, a_end: Optional[str], b_start: str, b_end: Optional[str]) -> bool:
    a_end_dt = datetime.fromisoformat(a_end) if a_end else datetime.now(timezone.utc)
    b_end_dt = datetime.fromisoformat(b_end) if b_end else datetime.now(timezone.utc)
    return datetime.fromisoformat(a_start) < b_end_dt and datetime.fromisoformat(b_start) < a_end_dt


async def _collect_employee_warnings(tenant_id: str, employee: dict, timesheet: dict) -> list[str]:
    warnings: list[str] = []
    if int(employee.get("hourly_rate_cents", 0) or 0) <= 0:
        warnings.append("missing_rate: Employee hourly rate is not set")
    if timesheet.get("status") != "approved":
        warnings.append("timesheet_not_approved: Weekly timesheet has not been approved yet")
    if timesheet.get("incomplete_entry_count", 0) > 0:
        warnings.append(f"open_time_entries: {timesheet['incomplete_entry_count']} time entry(ies) are missing a clock-out")
    entries = [e async for e in db.time_entries.find(
        {"tenant_id": tenant_id, "employee_id": employee["id"], "work_date": {"$gte": timesheet["week_start"], "$lte": timesheet["week_end"]},
         "status": {"$ne": "voided"}}, {"_id": 0, "clock_in_at": 1, "clock_out_at": 1},
    ).sort("clock_in_at", 1)]
    for i in range(len(entries) - 1):
        if _overlaps(entries[i]["clock_in_at"], entries[i].get("clock_out_at"), entries[i + 1]["clock_in_at"], entries[i + 1].get("clock_out_at")):
            warnings.append("overlapping_entries: Two or more time entries overlap for this employee this week")
            break
    return warnings


async def _sum_ledger(tenant_id: str, employee_id: str, period_id: str) -> dict[str, int]:
    sums: dict[str, int] = {}
    pipeline = [
        {"$match": {"tenant_id": tenant_id, "employee_id": employee_id, "pay_period_id": period_id,
                     "voided": False, "type": {"$ne": "void"}}},
        {"$group": {"_id": "$type", "total": {"$sum": "$amount_cents"}}},
    ]
    async for row in db.payroll_transactions.aggregate(pipeline):
        sums[row["_id"]] = int(row["total"])
    return sums


def _money_totals(sums: dict[str, int]) -> dict[str, int]:
    gross_regular = sums.get("earning", 0)
    gross_overtime = sums.get("overtime_earning", 0)
    adjustment = sums.get("adjustment", 0) + sums.get("correction", 0)
    advance = sums.get("advance", 0)
    repayment = sums.get("advance_repayment", 0)
    payment = sums.get("payment", 0)
    carryover_in = sums.get("carryover_in", 0)
    carryover_out = sums.get("carryover_out", 0)
    total_earned = gross_regular + gross_overtime + adjustment + carryover_in - repayment
    total_paid = advance + payment
    remaining = total_earned - total_paid - carryover_out
    return {
        "gross_regular_cents": gross_regular, "gross_overtime_cents": gross_overtime,
        "adjustment_total_cents": adjustment, "advance_total_cents": advance,
        "repayment_total_cents": repayment, "payment_total_cents": payment,
        "carryover_in_cents": carryover_in, "carryover_out_cents": carryover_out,
        "total_earned_cents": total_earned, "total_paid_cents": total_paid,
        "remaining_balance_cents": remaining,
    }


async def recompute_snapshot_totals(*, tenant_id: str, period_id: str, employee_id: str) -> dict:
    """Re-derive a snapshot's MONEY fields from the ledger only (hours/rate untouched).
    Creates a minimal snapshot row if one doesn't exist yet (e.g. an advance
    recorded before the first recalculation)."""
    sums = await _sum_ledger(tenant_id, employee_id, period_id)
    totals = _money_totals(sums)
    existing = await db.payroll_snapshots.find_one({"tenant_id": tenant_id, "pay_period_id": period_id, "employee_id": employee_id}, {"_id": 0})
    now_iso = utc_now().isoformat()
    if existing:
        await db.payroll_snapshots.update_one(
            {"id": existing["id"], "tenant_id": tenant_id},
            {"$set": {**totals, "updated_at": now_iso}},
        )
    else:
        emp = await db.employees.find_one({"id": employee_id, "tenant_id": tenant_id}, {"_id": 0})
        if not emp:
            raise PayrollError(404, "Employee not found")
        doc = PayrollSnapshot(
            tenant_id=tenant_id, pay_period_id=period_id, employee_id=employee_id, employee_name=emp["name"],
            employee_status=emp["status"], linked_user_id=emp.get("linked_user_id"),
            hourly_rate_cents=emp.get("hourly_rate_cents", 0), **totals,
            created_by=SYSTEM_ACTOR, updated_by=SYSTEM_ACTOR,
        ).model_dump()
        await db.payroll_snapshots.insert_one(prepare_for_mongo(dict(doc)))
    doc = await db.payroll_snapshots.find_one({"tenant_id": tenant_id, "pay_period_id": period_id, "employee_id": employee_id}, {"_id": 0})
    await _refresh_period_financial_status(tenant_id, period_id)
    return serialize_doc(doc or {})


async def _replace_generated_transaction(tenant_id: str, employee_id: str, period: dict, txn_type: str,
                                          new_amount_cents: int, timesheet_id: str) -> None:
    """Idempotent regeneration: only void + recreate when the computed amount
    actually changed. Clicking Recalculate repeatedly with unchanged hours
    never creates duplicate ledger rows."""
    existing = await db.payroll_transactions.find_one(
        {"tenant_id": tenant_id, "employee_id": employee_id, "pay_period_id": period["id"], "type": txn_type, "voided": False},
        {"_id": 0},
    )
    if existing and existing["amount_cents"] == new_amount_cents:
        return
    now_iso = utc_now().isoformat()
    if existing:
        await db.payroll_transactions.update_one(
            {"id": existing["id"], "tenant_id": tenant_id},
            {"$set": {"voided": True, "voided_by": SYSTEM_ACTOR, "voided_at": now_iso, "void_reason": "superseded_by_recalculation"}},
        )
    if new_amount_cents > 0:
        doc = PayrollTransaction(
            tenant_id=tenant_id, employee_id=employee_id, pay_period_id=period["id"], type=txn_type,
            amount_cents=new_amount_cents, effective_date=period["end_date"],
            reference=f"{'Regular' if txn_type == 'earning' else 'Overtime'} pay {period['start_date']}\u2013{period['end_date']}",
            source_record_type="timesheet", source_record_id=timesheet_id, created_by=SYSTEM_ACTOR,
        ).model_dump()
        await db.payroll_transactions.insert_one(prepare_for_mongo(dict(doc)))


async def recalc_employee_snapshot(*, tenant_id: str, period: dict, employee_id: str) -> dict:
    if period["status"] not in ("open", "review"):
        raise PayrollError(409, f"Pay Period is {period['status']} — reopen it before recalculating")
    emp = await db.employees.find_one({"id": employee_id, "tenant_id": tenant_id}, {"_id": 0})
    if not emp:
        raise PayrollError(404, "Employee not found")
    from . import timesheet_service
    ts = await timesheet_service.get_or_create_weekly_timesheet(tenant_id=tenant_id, employee_id=employee_id, week_start=period["start_date"])
    tenant_settings = await get_payroll_settings(tenant_id=tenant_id)
    ot_policy = _resolve_overtime_policy(tenant_settings, emp)
    worked_minutes = ts["worked_minutes"]
    if ot_policy["enabled"]:
        threshold = ot_policy["weekly_threshold_minutes"]
        regular_minutes = min(worked_minutes, threshold)
        overtime_minutes = max(worked_minutes - threshold, 0)
    else:
        regular_minutes, overtime_minutes = worked_minutes, 0
    rate = int(emp.get("hourly_rate_cents", 0) or 0)
    gross_regular_cents = round(regular_minutes / 60 * rate)
    gross_overtime_cents = round(overtime_minutes / 60 * rate * ot_policy["multiplier"])
    warnings = await _collect_employee_warnings(tenant_id, emp, ts)

    await _replace_generated_transaction(tenant_id, employee_id, period, "earning", gross_regular_cents, ts["id"])
    await _replace_generated_transaction(tenant_id, employee_id, period, "overtime_earning", gross_overtime_cents, ts["id"])

    sums = await _sum_ledger(tenant_id, employee_id, period["id"])
    totals = _money_totals(sums)
    existing = await db.payroll_snapshots.find_one({"tenant_id": tenant_id, "pay_period_id": period["id"], "employee_id": employee_id}, {"_id": 0})
    now_iso = utc_now().isoformat()
    fields = {
        "employee_name": emp["name"], "employee_status": emp["status"], "linked_user_id": emp.get("linked_user_id"),
        "hourly_rate_cents": rate, "overtime_policy_snapshot": ot_policy,
        "regular_minutes": regular_minutes, "overtime_minutes": overtime_minutes,
        **totals, "calculated_at": now_iso, "source_timesheet_ids": [ts["id"]], "warnings": warnings,
        "locked": False, "updated_at": now_iso, "updated_by": SYSTEM_ACTOR,
    }
    if existing:
        await db.payroll_snapshots.update_one(
            {"id": existing["id"], "tenant_id": tenant_id},
            {"$set": fields, "$inc": {"calculation_version": 1}},
        )
    else:
        doc = PayrollSnapshot(tenant_id=tenant_id, pay_period_id=period["id"], employee_id=employee_id,
                               calculation_version=1, created_by=SYSTEM_ACTOR, **fields).model_dump()
        await db.payroll_snapshots.insert_one(prepare_for_mongo(dict(doc)))
    doc = await db.payroll_snapshots.find_one({"tenant_id": tenant_id, "pay_period_id": period["id"], "employee_id": employee_id}, {"_id": 0})
    await _refresh_period_financial_status(tenant_id, period["id"])
    return serialize_doc(doc or {})


async def _candidate_employee_ids(tenant_id: str, period: dict) -> set[str]:
    ids: set[str] = set()
    async for t in db.timesheets.find({"tenant_id": tenant_id, "week_start": period["start_date"]}, {"_id": 0, "employee_id": 1}):
        ids.add(t["employee_id"])
    async for s in db.payroll_snapshots.find({"tenant_id": tenant_id, "pay_period_id": period["id"]}, {"_id": 0, "employee_id": 1}):
        ids.add(s["employee_id"])
    async for tx in db.payroll_transactions.find({"tenant_id": tenant_id, "pay_period_id": period["id"], "voided": False}, {"_id": 0, "employee_id": 1}):
        ids.add(tx["employee_id"])
    return ids


async def recalc_period(*, tenant_id: str, period_id: str, actor_user_id: str, actor_email: str) -> dict:
    period = await _get_period(tenant_id, period_id)
    if period["status"] not in ("open", "review"):
        raise PayrollError(409, f"Pay Period is {period['status']} — reopen it before recalculating")
    employee_ids = await _candidate_employee_ids(tenant_id, period)
    for emp_id in employee_ids:
        await recalc_employee_snapshot(tenant_id=tenant_id, period=period, employee_id=emp_id)
    await record_activity_with_audit(
        tenant_id=tenant_id, actor_user_id=actor_user_id, actor_email=actor_email,
        module="team", action="payroll.period_recalculated", entity_type="pay_period", entity_id=period_id,
        summary=f"Pay Period {period['start_date']}\u2013{period['end_date']} recalculated ({len(employee_ids)} employee(s))",
    )
    return await get_pay_period_detail(tenant_id=tenant_id, period_id=period_id)


