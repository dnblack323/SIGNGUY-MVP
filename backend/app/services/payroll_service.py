async def _collect_blocking_warnings(tenant_id: str, period: dict) -> list[dict]:
    blocking: list[dict] = []
    snapshots = await list_period_snapshots(tenant_id=tenant_id, period_id=period["id"])
    snapshotted_ids = {s["employee_id"] for s in snapshots}
    for s in snapshots:
        if s.get("warnings"):
            blocking.append({"employee_id": s["employee_id"], "employee_name": s["employee_name"], "warnings": s["warnings"]})
    async for t in db.timesheets.find({"tenant_id": tenant_id, "week_start": period["start_date"]}, {"_id": 0, "employee_id": 1}):
        if t["employee_id"] not in snapshotted_ids:
            emp = await db.employees.find_one({"id": t["employee_id"], "tenant_id": tenant_id}, {"_id": 0, "name": 1})
            blocking.append({"employee_id": t["employee_id"], "employee_name": (emp or {}).get("name", t["employee_id"]),
                              "warnings": ["not_recalculated: Employee has hours logged this week but has not been included in a Recalculate yet"]})
    return blocking


